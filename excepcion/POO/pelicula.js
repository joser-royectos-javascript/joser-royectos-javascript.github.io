// Clases
// Clase Pelicula
class Pelicula {
    // Constructor de la clase
    constructor(titulo, director, año) {
        // Atributos o propiedades de la clase
        this.titulo = titulo;
        this.director = director;
        this.año = año;
        this.reproduciendo = false;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    static agregarPeli(titulo, director, año) {
        const nuevaPelicula = new Pelicula(titulo, director, año);
        console.log(`Nueva película agregada: ${titulo} (${año}), dirigida por ${director}.`);
        return nuevaPelicula;
    }
    // Método para reproducir la película
    reproducir() {
        this.reproduciendo = true;
        console.log(`Reproduciendo ${this.titulo} (${this.año}), dirigida por ${this.director}.`);
    }
    // Método para detener la reproducción de la película
    detener() {
        this.reproduciendo = false;
        console.log(`Deteniendo ${this.titulo}.`);
    }
    // Método saludar
    saludar(){
        console.log(`Hola, soy ${this.titulo}`)
    }
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Crear objetos a partir de la clase Pelicula
const pelicula1 = new Pelicula("El Padrino", "Francis Ford Coppola", 1972);
const pelicula2 = new Pelicula("Titanic", "James Cameron", 1997);
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Agregar una nueva película utilizando el método estático agregarPeli
const nuevaPelicula = Pelicula.agregarPeli("Interestelar", "Christopher Nolan", 2014);
nuevaPelicula.reproducir();
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Llamar a métodos de los objetos
pelicula1.reproducir();
pelicula2.detener();
nuevaPelicula.saludar();
