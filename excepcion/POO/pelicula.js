// Clase: Una clase es una plantilla o un plano para crear objetos. 
// Define las propiedades (atributos) y comportamientos (métodos) que los objetos creados a partir de ella tendrán. Por ejemplo:
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
